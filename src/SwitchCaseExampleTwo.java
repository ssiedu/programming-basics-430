
public class SwitchCaseExampleTwo {

    public static void main(String[] args) {

        String state=args[0];
        switch(state){
            case "mp":
                System.out.println("bhopal");
            break;
            case "mh":
                System.out.println("mumbai");
            break;
            case "rj":
                System.out.println("jaipur");
            break;
        }
        
    }
}
